// CIC filter
function theCIC (taps) {
    var i,
        integrator = 0,
        comb = [],
        pointer = 0;

    for (i = 0; i < taps; i++) {
        comb.push(0);
    }

    return function (x) {
        var tmp;
        tmp = comb[pointer];
        comb[pointer] = x;
        pointer++;
        if (pointer === taps) { pointer = 0; }

        x -= tmp; // comb

        x += integrator; // integrator
        integrator = x;
        // console.log(x);
        return x;
    };
}

